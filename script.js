// Запрос подтверждения сброса игры
function confirmResetProgress() {
    const lang = gameState.currentLang;
    const confirmText = uiTranslations[lang].resetConfirm;
    if (confirm(confirmText)) {
        resetProgress();
    }
}

// Полный сброс всех данных
function resetProgress() {
    const lang = gameState.currentLang;
    
    const keysToRemove = [];
    for (let i = 0; i < localStorage.length; i++) {
        const key = localStorage.key(i);
        if (key.startsWith("track_") || key.startsWith("ark_")) {
            keysToRemove.push(key);
        }
    }
    keysToRemove.forEach(k => localStorage.removeItem(k));
    localStorage.removeItem("ark_survival_save");
    
    gameState = {
        currentLang: lang,
        currentDayKey: "intro",
        currentStepIndex: 0,
        waterScore: 0,
        workoutScore: 0,
        unlockedDays: ["intro"]
    };
    
    saveProgress();
    window.location.reload();
}

