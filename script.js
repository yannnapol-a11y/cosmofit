// Генерация звездного фона
function generateStarfield() {
    const starsContainer = document.querySelector(".stars");
    if (!starsContainer) return;
    starsContainer.innerHTML = "";
    const starCount = 80;
    for (let i = 0; i < starCount; i++) {
        const star = document.createElement("div");
        star.className = "star";
        const size = Math.random() * 2.5 + 0.5;
        star.style.width = `${size}px`;
        star.style.height = `${size}px`;
        star.style.top = `${Math.random() * 100}%`;
        star.style.left = `${Math.random() * 100}%`;
        star.style.animationDelay = `${Math.random() * 4}s`;
        star.style.animationDuration = `${Math.random() * 3 + 2}s`;
        starsContainer.appendChild(star);
    }
}

// Загрузка игрового прогресса
function loadProgress() {
    const saved = localStorage.getItem("ark_survival_save");
    if (saved) {
        gameState = JSON.parse(saved);
        if (!gameState.currentLang) {
            gameState.currentLang = localStorage.getItem("ark_survival_lang") || "en";
        }
    }
}

// Сохранение игрового прогресса
function saveProgress() {
    localStorage.setItem("ark_survival_save", JSON.stringify(gameState));
}

// Изменение текущего языка в выпадающем списке
function changeLanguage(lang) {
    gameState.currentLang = lang;
    localStorage.setItem("ark_survival_lang", lang);
    
    updateInterfaceTexts();
    renderCurrentState();
}

// Обновление всех статичных элементов под выбранный язык
function updateInterfaceTexts() {
    const lang = gameState.currentLang;
    const ui = uiTranslations[lang];
    
    const langSelect = document.getElementById("lang-select");
    if (langSelect) langSelect.value = lang;
    
    const statusInd = document.getElementById("status-indicator");
    if (statusInd) statusInd.innerText = ui.systemOnline;
    
    const missionBox = document.getElementById("ship-mission");
    if (missionBox) missionBox.innerText = ui.missionText;
    
    const resetBtn = document.getElementById("reset-btn");
    if (resetBtn) resetBtn.innerText = ui.resetBtn;
    
    const lblWater = document.getElementById("label-water");
    if (lblWater) lblWater.innerText = ui.hudWater;
    const lblEnergy = document.getElementById("label-energy");
    if (lblEnergy) lblEnergy.innerText = ui.hudEnergy;
    
    const footerThanks = document.getElementById("footer-thanks");
    if (footerThanks) footerThanks.innerHTML = ui.footerThanks;
    
    updateNavigationButtons();
}

// Обновление состояния кнопок дней в меню
function updateNavigationButtons() {
    const lang = gameState.currentLang;
    const ui = uiTranslations[lang];
    
    const dayKeys = ["intro", "day1", "day2", "day3", "day4", "day5", "day6", "day7", "final"];
    dayKeys.forEach(key => {
        const btnId = key === "final" ? "btn-final" : `btn-${key}`;
        const btn = document.getElementById(btnId);
        if (btn) {
            const isUnlocked = gameState.unlockedDays.includes(key);
            btn.innerHTML = isUnlocked ? ui.days[key] : ui.days[key] + " 🔒";
            if (isUnlocked) {
                btn.removeAttribute("disabled");
                btn.classList.remove("locked");
            } else {
                btn.setAttribute("disabled", "true");
                btn.classList.add("locked");
            }
        }
    });
}

// Переключение между вкладками дней через верхнее меню
function loadDay(dayKey) {
    if (!gameState.unlockedDays.includes(dayKey)) return;
    
    gameState.currentDayKey = dayKey;
    gameState.currentStepIndex = 0;
    saveProgress();
    
    document.querySelectorAll(".nav-btn").forEach(btn => btn.classList.remove("active"));
    const activeBtn = document.getElementById(dayKey === "final" ? "btn-final" : `btn-${dayKey}`);
    if (activeBtn) activeBtn.classList.add("active");
    
    if (dayKey === "final" && gameState.currentStepIndex >= storyData.final.steps[gameState.currentLang].length - 1) {
        renderVictoryScreen();
    } else {
        renderCurrentState();
        updateHUD();
    }
}

// Отрисовка текущего экрана (Интро, Сюжет или Карточка задач)
function renderCurrentState() {
    const currentDay = storyData[gameState.currentDayKey];
    const textElement = document.getElementById("story-text");
    const actionBtn = document.getElementById("action-btn");
    
    if (!textElement || !actionBtn) return;
    
    actionBtn.style.display = "block";
    
    const lang = gameState.currentLang;
    
    if (currentDay.type === "text-only") {
        textElement.innerHTML = `<span class="story-paragraph">${currentDay.steps[lang][gameState.currentStepIndex]}</span>`;
        actionBtn.innerText = currentDay.btnTexts[lang][gameState.currentStepIndex];
        actionBtn.onclick = handleAction;
    } else if (currentDay.type === "interactive-day") {
        let isCompleted = isDayCompleted(gameState.currentDayKey);
        
        if (gameState.currentStepIndex === 0) {
            textElement.innerHTML = `<span class="story-paragraph">${currentDay.introText[lang]}</span>`;
            actionBtn.innerText = uiTranslations[lang].confirmProtocol;
            actionBtn.onclick = () => {
                gameState.currentStepIndex = 1;
                saveProgress();
                renderCurrentState();
            };
        } else {
            renderInteractiveDashboard(currentDay, textElement, actionBtn, isCompleted);
        }
    }
}

// Обработка кликов по кнопкам на текстовых экранах
function handleAction() {
    const currentDay = storyData[gameState.currentDayKey];
    const lang = gameState.currentLang;
    
    if (currentDay.type === "text-only") {
        if (gameState.currentStepIndex < currentDay.steps[lang].length - 1) {
            gameState.currentStepIndex++;
            saveProgress();
            renderCurrentState();
        } else {
            if (currentDay.nextKey) {
                unlockNextDay(currentDay.nextKey);
            }
        }
    }
}

// Разблокировка следующего дня
function unlockNextDay(nextKey) {
    if (!gameState.unlockedDays.includes(nextKey)) {
        gameState.unlockedDays.push(nextKey);
    }
    gameState.currentDayKey = nextKey;
    gameState.currentStepIndex = 0;
    saveProgress();
    
    document.querySelectorAll(".nav-btn").forEach(btn => btn.classList.remove("active"));
    const activeBtn = document.getElementById(nextKey === "final" ? "btn-final" : `btn-${nextKey}`);
    if (activeBtn) activeBtn.classList.add("active");
    
    updateNavigationButtons();
    renderCurrentState();
    updateHUD();
}

// Проверка выполнения всех задач за день
function isDayCompleted(dayKey) {
    const day = localStorage.getItem(`ark_done_${dayKey}`);
    return day === "true";
}

// Отрисовка интерактивной панели дня с чекбоксами и формой тренировок
function renderInteractiveDashboard(currentDay, textElement, actionBtn, isCompleted) {
    const dayKey = gameState.currentDayKey;
    const isWaterDone = localStorage.getItem(`track_${dayKey}_water`) === "true";
    const workoutReps = localStorage.getItem(`track_${dayKey}_workout`) || 0;
    
    const lang = gameState.currentLang;
    const ui = uiTranslations[lang];
    
    let html = `
        <div class="interactive-card">
            <h3 class="card-title">${currentDay.title[lang]}</h3>
            <p class="card-subtitle">${ui.interactiveTitleTasks}</p>
            <div class="tasks-list">
    `;
    
    currentDay.tasks.forEach(task => {
        let isChecked = false;
        if (task.id === "water") isChecked = isWaterDone;
        
        html += `
            <label class="task-item ${isChecked ? 'completed' : ''}">
                <input type="checkbox" data-id="${task.id}" ${isChecked ? 'checked' : ''} ${isCompleted ? 'disabled' : ''} onchange="toggleTask('${task.id}', this.checked)">
                <span class="custom-checkbox"></span>
                <span class="task-text">${task.text[lang]}</span>
            </label>
        `;
    });
    
    html += `
            </div>
            <div class="workout-section">
                <p class="card-subtitle" style="margin-top:15px;">${ui.interactiveTitleWorkout}</p>
                <p class="task-text" style="margin-bottom:10px;">${currentDay.workoutText[lang]}</p>
                <div class="workout-form">
                    <label class="task-text">${ui.interactiveRepsLabel}</label>
                    <input type="number" id="workout-input" class="workout-input" value="${workoutReps}" min="0" ${isCompleted ? 'disabled' : ''}>
                </div>
            </div>
        </div>
    `;
    
    textElement.innerHTML = html;
    
    if (!isCompleted) {
        actionBtn.innerText = ui.interactiveSubmitBtn;
        actionBtn.onclick = submitWorkoutResults;
    } else {
        actionBtn.innerText = ui.interactiveNextDayBtn;
        actionBtn.onclick = () => unlockNextDay(currentDay.nextKey);
    }
}

// Переключение галочки на интерактивной задаче
function toggleTask(taskId, isChecked) {
    const dayKey = gameState.currentDayKey;
    localStorage.setItem(`track_${dayKey}_${taskId}`, isChecked ? "true" : "false");
    
    const labels = document.querySelectorAll(".task-item");
    labels.forEach(label => {
        const input = label.querySelector("input");
        if (input && input.getAttribute("data-id") === taskId) {
            if (isChecked) label.classList.add("completed");
            else label.classList.remove("completed");
        }
    });
    
    updateHUD();
}

// Клик по кнопке сохранения результатов дня
function submitWorkoutResults() {
    const dayKey = gameState.currentDayKey;
    const input = document.getElementById("workout-input");
    if (!input) return;
    
    let reps = parseInt(input.value) || 0;
    if (reps < 0) reps = 0;
    
    localStorage.setItem(`track_${dayKey}_workout`, reps);
    localStorage.setItem(`ark_done_${dayKey}`, "true");
    
    calcResult(dayKey, reps);
    updateHUD();
    renderCurrentState();
}

// Расчет очков за день
function calcResult(dayKey, reps) {
    const isWater = localStorage.getItem(`track_${dayKey}_water`) === "true";
    if (isWater) gameState.waterScore += 20;
    gameState.workoutScore += reps;
    
    saveProgress();
}

// Обновление шкал HUD
function updateHUD() {
    let totalWater = 0, totalWorkouts = 0;
    let dayCount = 0;
    
    const dayKeys = ["day1", "day2", "day3", "day4", "day5", "day6", "day7"];
    
    dayKeys.forEach(key => {
        if (gameState.unlockedDays.includes(key)) {
            dayCount++;
            if (localStorage.getItem(`track_${key}_water`) === "true") totalWater += 100;
            
            let reps = parseInt(localStorage.getItem(`track_${key}_workout`)) || 0;
            totalWorkouts += reps;
        }
    });
    
    let waterPercent = dayCount > 0 ? Math.round(totalWater / dayCount) : 0;
    
    let energyPercent = 0;
    if (dayCount > 0) {
        let averageReps = totalWorkouts / dayCount;
        if (averageReps >= 35) energyPercent = 100;
        else if (averageReps >= 20) energyPercent = 75;
        else if (averageReps >= 10) energyPercent = 45;
        else if (averageReps > 0) energyPercent = 20;
    }
    
    const fillWater = document.getElementById("hud-water");
    const txtWater = document.getElementById("hud-water-txt");
    if (fillWater && txtWater) { fillWater.style.width = `${waterPercent}%`; txtWater.innerText = `${waterPercent}%`; }
    
    const fillEnergy = document.getElementById("hud-energy");
    const txtEnergy = document.getElementById("hud-energy-txt");
    if (fillEnergy && txtEnergy) { fillEnergy.style.width = `${energyPercent}%`; txtEnergy.innerText = `${energyPercent}%`; }
}

// Окно победы на финальном экране
